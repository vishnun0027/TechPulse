-- 7. RPC FUNCTIONS

-- Semantic search per user
CREATE OR REPLACE FUNCTION match_articles(
  query_embedding  vector(768),
  match_threshold  float,
  match_count      int,
  p_user_id        uuid
) RETURNS TABLE (id uuid, title text, summary text, why_it_matters text, published_at timestamptz, similarity float)
LANGUAGE sql STABLE AS $$
  SELECT id, title, summary, why_it_matters, published_at, 1 - (embedding <=> query_embedding) AS similarity
  FROM articles WHERE user_id = p_user_id AND embedding IS NOT NULL AND 1 - (embedding <=> query_embedding) > match_threshold
  ORDER BY similarity DESC LIMIT match_count;
$$;

-- Near-duplicate check
CREATE OR REPLACE FUNCTION is_near_duplicate(query_embedding vector(768), dup_threshold float, p_user_id uuid)
RETURNS boolean LANGUAGE sql STABLE AS $$
  SELECT EXISTS (SELECT 1 FROM articles WHERE user_id = p_user_id AND embedding IS NOT NULL AND 1 - (embedding <=> query_embedding) >= dup_threshold);
$$;

-- Recency-weighted similarity (fuses content similarity with freshness)
CREATE OR REPLACE FUNCTION match_articles_recency(
  query_embedding  vector(768),
  match_count      int,
  p_user_id        uuid,
  decay_rate       float DEFAULT 0.15
)
RETURNS TABLE (id uuid, title text, summary text, recency_score float)
LANGUAGE sql STABLE AS $$
  SELECT
    id, title, summary,
    (1 - (embedding <=> query_embedding))
      * exp(-decay_rate * EXTRACT(EPOCH FROM (now() - COALESCE(published_at, created_at))) / 86400)
    AS recency_score
  FROM articles
  WHERE user_id = p_user_id
    AND embedding IS NOT NULL
  ORDER BY recency_score DESC
  LIMIT match_count;
$$;

-- Atomic increment for source ingestion tracking
CREATE OR REPLACE FUNCTION increment_source_ingestion(p_source_id bigint, p_user_id uuid)
RETURNS void LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
    INSERT INTO source_health (source_id, user_id, articles_ingested, updated_at)
    VALUES (p_source_id, p_user_id, 1, now())
    ON CONFLICT (source_id, user_id)
    DO UPDATE SET articles_ingested = source_health.articles_ingested + 1, updated_at = now();
END;
$$;

-- Atomic increment for article delivery tracking
CREATE OR REPLACE FUNCTION increment_source_delivery(p_source_id bigint, p_user_id uuid)
RETURNS void LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
    INSERT INTO source_health (source_id, user_id, articles_delivered, articles_clicked, quality_score, updated_at)
    VALUES (p_source_id, p_user_id, 1, 0, 0.5, now())
    ON CONFLICT (source_id, user_id)
    DO UPDATE SET
        articles_delivered = source_health.articles_delivered + 1,
        quality_score = GREATEST(0.1, LEAST(1.0,
            CASE
                WHEN (source_health.articles_delivered + 1) > 0
                THEN ROUND(source_health.articles_clicked::numeric / (source_health.articles_delivered + 1), 4)
                ELSE 0.5
            END
        )),
        updated_at = now();
END;
$$;

-- Atomic increment for article click tracking (called from the frontend)
CREATE OR REPLACE FUNCTION increment_source_click(p_source_id bigint, p_user_id uuid)
RETURNS void LANGUAGE plpgsql SECURITY DEFINER AS $$
BEGIN
    INSERT INTO source_health (source_id, user_id, articles_clicked, articles_delivered, quality_score, updated_at)
    VALUES (p_source_id, p_user_id, 1, 0, 0.5, now())
    ON CONFLICT (source_id, user_id)
    DO UPDATE SET
        articles_clicked = source_health.articles_clicked + 1,
        quality_score = GREATEST(0.1, LEAST(1.0,
            CASE
                WHEN source_health.articles_delivered > 0
                THEN ROUND((source_health.articles_clicked + 1)::numeric / source_health.articles_delivered, 4)
                ELSE 0.5
            END
        )),
        updated_at = now();
END;
$$;

-- Match thematic clusters
CREATE OR REPLACE FUNCTION match_events_by_centroid(query_embedding vector(768), threshold float, p_user_id uuid)
RETURNS TABLE (id uuid, article_count int, similarity float)
LANGUAGE sql STABLE AS $$
  SELECT id, article_count, 1 - (centroid_embedding <=> query_embedding) AS similarity
  FROM article_events WHERE user_id = p_user_id AND centroid_embedding IS NOT NULL AND 1 - (centroid_embedding <=> query_embedding) >= threshold
  ORDER BY similarity DESC LIMIT 5;
$$;

